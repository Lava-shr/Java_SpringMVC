package com.elec5619.sr.service;


/*
 * Class to display list of students in JSP
 * 
 */
public class ListOfStudents {
	
	String username;
	String email;
	String group_no;
	String group_limit;
	String avg_ratings;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getGroup_no() {
		return group_no;
	}
	public void setGroup_no(String group_no) {
		this.group_no = group_no;
	}
	public String getGroup_limit() {
		return group_limit;
	}
	public void setGroup_limit(String group_limit) {
		this.group_limit = group_limit;
	}
	public String getAvg_ratings() {
		return avg_ratings;
	}
	public void setAvg_ratings(String avg_ratings) {
		this.avg_ratings = avg_ratings;
	}
	

}
